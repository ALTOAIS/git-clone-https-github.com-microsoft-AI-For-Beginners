export async function seedRiskTemplates(prisma: PrismaClient, categoryIds: Record<string, string>) {
  const existingCodes = new Set(
    (await prisma.riskTemplate.findMany({ select: { code: true } })).map((r) => r.code),
  );
  let nextSeq = existingCodes.size + 1;
  const nextCode = () => {
    let code = `RT-${String(nextSeq).padStart(4, '0')}`;
    while (existingCodes.has(code)) {
      nextSeq += 1;
      code = `RT-${String(nextSeq).padStart(4, '0')}`;
    }
    existingCodes.add(code);
    nextSeq += 1;
    return code;
  };

  let created = 0;
  let total = 0;
  for (const block of BLOCKS) {
    const categoryId = categoryIds[block.categoryName];
    for (const item of block.items) {
      total += 1;
      const exists = await prisma.riskTemplate.findFirst({ where: { title: item.title, direction: block.direction } });
      if (exists) continue;
      await prisma.riskTemplate.create({
        data: {
          code: nextCode(),
          title: item.title,
          categoryId,
          direction: block.direction,
          description: item.description,
          corruptionScheme: item.corruptionScheme,
          causes: item.causes,
          corruptionFactors: item.corruptionFactors,
          consequences: item.consequences,
          redFlags: item.redFlags,
          typicalControls: item.typicalControls,
          recommendedActions: item.recommendedActions,
          baseProbability: item.baseProbability,
          baseImpact: item.baseImpact,
          baseScore: item.baseProbability * item.baseImpact,
          tags: item.tags,
        },
      });
      created += 1;
    }
  }
  console.log(`Библиотека типовых рисков: создано ${created} из ${total} шаблонов (направлений: ${BLOCKS.length}).`);
}
