/**
 * Зеркало backend'овой normalizeEn (ai/fallbacks.ts) — используется только
 * для мгновенной локальной проверки ответа кнопкой «Проверить ответ»
 * (раздел 1 доработок). Финальный результат всегда пересчитывается и
 * сохраняется на бэкенде через «Завершить упражнение», эта функция ни на
 * что не влияет, кроме предпросмотра.
 */
export function normalizeEn(text: string): string {
  return text
    .toLowerCase()
    .replace(/[’']/g, "'")
    .replace(/[.,!?;:"()«»—-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}
